# Enter the marks of students and thenassign the grades 

marks = int(input("Ente the marks of students ")) 

if 100 >= marks >= 91 : print("A Grade") 

elif 90 >= marks >= 81: print("B Grade")

elif 80 >= marks >= 71 : print("C Grade") 

elif 70 >= marks >= 61 : print("D Grade") 

elif 60 >= marks >= 51 : print("E Grade") 

else : print("F Grade") 