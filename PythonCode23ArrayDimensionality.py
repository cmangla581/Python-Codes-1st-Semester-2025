import numpy as np 
arr1 = np.array([10,20,30,40]) 

arr2 = np.array([[1,2,3], [4,5,6]]) 

arr3 = np.array([[[1,2], [3,4]], [[5,6], [7,8]]]) 

# Displaying of arrays 
print("1 D Array", arr1) 
print("Dimenasionality of Array 1 ", arr1.ndim)

print("2 D Array", arr2) 
print("Dimenasionality of Array 2 ", arr2.ndim)

print("3 D Array", arr3) 
print("Dimenasionality of Array 3 ", arr3.ndim)